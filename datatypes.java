class datatypes
{
 public static void main(String args[])
 {
  byte a=33;
  short s=999;
  int i=99000;
  long l=500000;
  float e=82.6f;
  double PI=3.14159;
  char c='S';
  String name="Sanath";
  boolean b1=true;

  System.out.println("Byte value a="+a);
  System.out.println("Short value s="+s);
  System.out.println("Integer value i="+i);
  System.out.println("Long value l="+l);
  System.out.println("Floating point e="+e);
  System.out.println("Double value PI="+PI);
  System.out.println("Character c="+c);
  System.out.println ("String name="+name);
  System.out.println ("Boolean value b1= "+b1);
 }
}
